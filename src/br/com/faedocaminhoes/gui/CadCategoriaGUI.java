    private CategoriaService setCategoriaService(CategoriaService categoriaService){
        return this.categoriaService = categoriaService;
    }
    
    private void setModel(){
        tableCategoria.setModel(tableModel);
        tableCategoria.setDefaultRenderer(Object.class, new CategoriaTableRenderer());
        tableCategoria.getColumnModel().getColumn(0).setPreferredWidth(50); //CODIGO
        tableCategoria.getColumnModel().getColumn(1).setPreferredWidth(300); //NOME
        tableCategoria.getColumnModel().getColumn(2).setPreferredWidth(80); //SIGLA
    }
    
    private void initComp(){
        setIco();
        setModel();
        findAll();
        txtDescricao.setDocument(new UpperCase());
        txtSigla.setDocument(new UpperCase());
        txtSearch.setDocument(new UpperCase());
        txtDescricao.requestFocus();
    }
    
    private void completeData(){
        categoria = new CategoriaProd();
        categoria.setId(ParseInteger.tryParseToInt(txtCod.getText()));
        categoria.setNome(txtDescricao.getText());
        categoria.setSigla(txtSigla.getText());
    }
    
    private void erasedComponents(){
        txtCod.setText("");
        txtDescricao.setText("");
        txtSigla.setText("");
        txtSearch.setText("");
        txtDescricao.requestFocus();
        btnDelete.setEnabled(false);
    }
    
    public void save(){
        completeData();
        if(categoriaService == null){
            JOptionPane.showMessageDialog(this, "CategoriaService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        categoriaService.insertOrUpdate(categoria);
        erasedComponents();
        findAll();
    }
    
    public void delete(){
        completeData();
        if(categoriaService == null){
            JOptionPane.showMessageDialog(this, "CategoriaService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        categoriaService.deleteById(categoria);
        erasedComponents();
        findAll();
        btnDelete.setEnabled(false);
    }
    
    public void findAll(){
        if(categoriaService == null){
            JOptionPane.showMessageDialog(this, "CategoriaService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        List<CategoriaProd> list = categoriaService.findAll();
        
        if(!list.isEmpty()){
            tableModel.removeAll();
            for(CategoriaProd u: categoriaService.findAll()){
                tableModel.addRow(u); 
            }            
        }else{
            throw new IllegalAccessError("List was null");
        }
    }
    
    private void findWithParameter(String pParam){
        if(categoriaService == null){
            JOptionPane.showMessageDialog(this, "CategoriaService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        List<CategoriaProd> list = categoriaService.findByName(pParam);        
        if(list.isEmpty()){
            findAll();
        }else{
            tableModel.removeAll();
            for(CategoriaProd p : list){
                tableModel.addRow(p);
            }  
        }        
    }
    
    private void getTable(){
        categoria = new CategoriaProd();
        if(tableCategoria.getSelectedRow() != -1){
            categoria = tableModel.getObject(tableCategoria.getSelectedRow());
            txtCod.setText(categoria.getId().toString());
            txtDescricao.setText(categoria.getNome());
            txtSigla.setText(categoria.getSigla());
            btnDelete.setEnabled(true);
        }
    }
    
    public CategoriaProd getObject(){
        return categoria;
    }
    
    public boolean verifyComp(){
        String err = "";
        
        if(txtDescricao.getText().equals("") || txtDescricao.getText().length() == 0){
            err += "O campo Descricão é obrigatório! \n";
        }
        if(txtSigla.getText().equals("") || txtSigla.getText().length() == 0){
            err += "O campo Sigla é obrigatório! \n";
        }
                
        if(err.length() == 0){
            return true;
        }else{
            JOptionPane.showMessageDialog(this, err,"FAEDO CAMINHÕES", JOptionPane.WARNING_MESSAGE);
            return false;
        }
    }
