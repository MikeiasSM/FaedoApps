    private void save(){
        completeData();
        if(vehicle == null){
            JOptionPane.showMessageDialog(this, "VehicleService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        vehicleService.insertOrUpdate(vehicle);
        erasedComponents();
        findAll();
    }
    
    private void delete(){
        vehicle = new Veiculo();
        completeData();
        if(vehicleService == null){
            JOptionPane.showMessageDialog(this, "VehicleService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        vehicleService.deleteById(vehicle);
        erasedComponents();
        findAll();
        btnDelete.setEnabled(false);
    }
    
    private void findAll(){
        if(vehicleService == null){
            JOptionPane.showMessageDialog(this, "VehicleService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        List<Veiculo> list = vehicleService.findAll();
        
        if(list != null){
            tableModel.removeAll();
            for(Veiculo p : list){
                tableModel.addRow(p);
            } 
        }else{
            throw new IllegalAccessError("List was null");
        }
    }
    
     private void findWithParameterIncrement(String pParam){
        if(vehicleService == null){
            JOptionPane.showMessageDialog(this, "ProviderService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        List<Veiculo> list = vehicleService.findByName(pParam);        
        if(list.isEmpty()){
            //findAll();
        }else{
            tableModel.removeAll();
            for(Veiculo p : list){
                tableModel.addRow(p);
            }  
        }        
    }
    
    private void findWithParameter(String pParam){
        if(vehicleService == null){
            JOptionPane.showMessageDialog(this, "ProviderService was null", "Error", JOptionPane.ERROR_MESSAGE);
        }
        List<Veiculo> list = vehicleService.findByName(pParam);        
        if(list.isEmpty()){
            findAll();
        }else{
            tableModel.removeAll();
            for(Veiculo p : list){
                tableModel.addRow(p);
            }  
        }        
    }
    
    private void popProvider(){
        cbProvider.removeAllItems();
        for(Fabricante p: providerService.findAll()){
            cbProvider.addItem(p);
        }
        cbProvider.setSelectedItem(null);
    }
    
    private void getTable(){
        vehicle = new Veiculo();
        if(tableVehicle.getSelectedRow() != -1){
            vehicle = tableModel.getObject(tableVehicle.getSelectedRow());
            txtCodVehicle.setText(vehicle.getId().toString());
            txtModelo.setText(vehicle.getModelo());
            txtCor.setText(vehicle.getCor());
            txtPlaca.setText(vehicle.getPlaca());
            cbProvider.setSelectedItem((Fabricante)vehicle.getProvider());
            btnDelete.setEnabled(true);
        }
    }
    
    private void setObject(Fabricante pProvider){
        if(pProvider != null){
            provider = pProvider;
        }
    }
    
